function add(n1,n2){
    return n1+n2
}
function sub(n1,n2){
    return n1-n2
}
function mul(n1,n2){
    return n1*n2
}
function div(n1,n2){
    return n1/n2
}

const res1=add(10,20)
console.log(`Addition - ${res1}`)

const res2=sub(20,10)
console.log(`Substraction - ${res2}`)

const res3=mul(10,20)
console.log(`Multiplication - ${res3}`)

const res4=div(10,2)
console.log(`Division - ${res4}`)