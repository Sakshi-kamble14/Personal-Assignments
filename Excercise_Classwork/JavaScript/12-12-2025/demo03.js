// Datatypes in js
let n1=10

console.log(`n1-${n1},typeof(n1)-${typeof(n1)}`)
// value -10 ,typeof - number

n1=10.22
console.log(`n1 -${n1},typeof(n1) -${typeof(n1)}`)
// value -10.22 ,typeof -number

n1=true
console.log(`n1 -${n1},typeof(n1)-${typeof(n1)}`)
// value -true, typeof - boolean

n1='sunbeam'
console.log(`n1 -${n1},typeof(n1)-${typeof(n1)}`)
// value -sunbeam,typeof - string

n1=null
console.log(`n1 -${n1},typeof(n1)-${typeof(n1)}`)
// value -null ,typeof - object

